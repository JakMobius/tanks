export const aliases: { [ key: string ]: string[] } = {}

function export_capabilities(entry: { [ key: string ]: string[] }) {
  for(let [key, item] of Object.entries(entry)) {
    aliases[key] = [item[0]]
  }
}

export_capabilities(bools)
export_capabilities(numbers)
export_capabilities(strings)

export const map: { [ key: string ]: string } = {}

for(let [key, value] of Object.entries(aliases)) {
  map[key] = key
  for(let alias of value) {
    map[alias] = key
  }
}

